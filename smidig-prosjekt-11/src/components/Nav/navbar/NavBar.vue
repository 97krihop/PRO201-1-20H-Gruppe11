<template>
  <div class="nav-bar">
    <hamburger-button @click="toggleMenu()" :is-selected="menuIsOpen" />

    <div class="logo-container">
      <router-link class="logo-link" to="/">
        <img id="logo-img" src="@/assets/Images/logo-bright.svg" alt="Bright" />
      </router-link>
    </div>
    <ul class="nav-menu" v-bind:class="{ active: menuIsOpen }">
      <nav-link
        :textValue="'Profile'"
        :icon="'user'"
        :linkDestination="'profile'"
      />

      <nav-link
        :textValue="'Certification'"
        :icon="'profile'"
        :linkDestination="'notimplemented'"
      />

      <nav-link
        :textValue="loginItem.itemTitle"
        :icon="loginItem.iconName"
        :linkDestination="loginItem.link"
      />
    </ul>
    <!-- <h1 class="user-header">{{ user || 'Not logged in' }}</h1> -->
  </div>
</template>
<script>
import HamburgerButton from "./HamburgerButton.vue";
import NavLink from "./NavLink.vue";
export default {
  components: { HamburgerButton, NavLink },
  name: "NavBar",
  data() {
    return {
      menuItems: [
        {
          itemTitle: "Profile",
          iconName: "user",
          link: "notimplemented",
          alt: "User icon"
        },
        {
          itemTitle: "Certifications",
          iconName: "profile",
          link: "notimplemented",
          alt: "Certifications icon"
        }
      ],
      menuIsOpen: false
    };
  },
  computed: {
    loginItem: function() {
      const userId = this.$store.getters.getUsername;

      if (!userId) {
        return {
          itemTitle: "Log in",
          iconName: "enter",
          link: "login",
          alt: "Login icon"
        };
      } else {
        return {
          itemTitle: "Log out",
          iconName: "exit",
          link: "logout",
          alt: "Logout icon"
        };
      }
    },
    isLoggedIn: function() {
      const userId = this.$store.getters.getUsername;
      if (!userId) {
        return false;
      } else {
        return true;
      }
    }
  },
  methods: {
    toggleMenu() {
      const temp = this.menuIsOpen;
      this.menuIsOpen = !temp;
    }
  }
};
</script>
