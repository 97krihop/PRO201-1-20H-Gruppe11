<template>
  <div class="container">
    <form @submit="onSubmit">
      <div class="wrapper">
        <div class="input">
          <label>Username: </label>
          <input
            v-model="username"
            type="text"
            placeholder="Enter Username"
            required
          />
          <br />
          <span>{{ usernameError }}</span>
        </div>
        <div class="input">
          <label>Password: </label>
          <input
            v-model="password"
            type="password"
            placeholder="Enter Password"
            required
          />
          <br />
          <span>{{ passwordError }}</span>
        </div>
        <div class="input">
          <label>Confirm Password: </label>
          <input
            v-model="confirmPassword"
            type="password"
            placeholder="Enter Password"
            required
          />
          <br />
          <span>{{ CPError }}</span>
        </div>
        <div class="admin-check">
          <label>Admin: </label>
          <input v-model="admin" type="checkbox" />
        </div>
        <div>
          <button id="submit-btn" type="submit" :disabled="isSubmitting">
            Submit
          </button>
        </div>
      </div>
    </form>
  </div>
</template>

<script>
import { useField, useForm } from "vee-validate";

export default {
  name: "UserAdministrationPage",
  setup() {
    const schema = {
      username(value) {
        return value && value.length > 6
          ? true
          : "This is username needs to be 6 or longer";
      },
      password(value) {
        return value && value.length >= 6
          ? true
          : "This is password needs to be 6 or longer";
      },
      confirmPassword(value) {
        return value === password.value
          ? true
          : "This is password needs to be 6 or longer";
      }
    };
    const { handleSubmit, isSubmitting } = useForm({
      validationSchema: schema
    });

    const onSubmit = handleSubmit((values, { resetForm }) => {
      console.log(values); // send data to API

      // reset the form and the field values to their initial values
      resetForm();
    });
    const { errorMessage: usernameError, value: username } = useField(
      "username"
    );
    const { errorMessage: passwordError, value: password } = useField(
      "password"
    );
    const { errorMessage: CPError, value: confirmPassword } = useField(
      "confirmPassword"
    );
    const { value: admin } = useField("admin");

    return {
      username,
      usernameError,
      password,
      passwordError,
      confirmPassword,
      CPError,
      isSubmitting,
      onSubmit,
      admin
    };
  }
};
</script>

